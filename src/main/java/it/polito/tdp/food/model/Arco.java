package it.polito.tdp.food.model;

public class Arco {
	
	private String p1;
	private String p2;
	private Integer peso;
	
	public Arco(String p1, String p2, Integer peso) {
		this.p1 = p1;
		this.p2 = p2;
		this.peso = peso;
	}
	
	public String getP1() {
		return p1;
	}
	
	public String getP2() {
		return p2;
	}
	
	public Integer getPeso() {
		return peso;
	}
	
	

}
